
/**
 * LocalDateTime Interface to be used when handleing Timezones
 *
 * @interface LocalDateTime
 * @typedef {LocalDateTime}
 */
interface LocalDateTime {
  
  /**
   * Date property
   * @type {string}
   */
  date: string,

  
  /**
   * Time property
   *
   * @type {string}
   */
  time: string
}